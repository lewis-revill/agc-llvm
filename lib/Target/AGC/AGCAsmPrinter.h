#ifndef LLVM_LIB_TARGET_AGC_AGCASMPRINTER_H
#define LLVM_LIB_TARGET_AGC_AGCASMPRINTER_H

#include "llvm/CodeGen/AsmPrinter.h"
#include <memory>

namespace llvm {
class MachineInstr;
class MCStreamer;
class StringRef;
class TargetMachine;

class AGCAsmPrinter : public AsmPrinter {
public:
  explicit AGCAsmPrinter(TargetMachine &TM,
                         std::unique_ptr<MCStreamer> Streamer)
      : AsmPrinter(TM, std::move(Streamer)) {}

  StringRef getPassName() const override { return "AGC Assembly Printer"; }

  void EmitInstruction(const MachineInstr *MI) override;

  // Generated by tablegen.
  bool emitPseudoExpansionLowering(MCStreamer &OutStreamer,
                                   const MachineInstr *MI);
};
} // namespace llvm

#endif // LLVM_LIB_TARGET_AGC_AGCASMPRINTER_H
